import { IProduct } from "../Interfaces/product.interface";
import mongoose from "mongoose";
import { IReview } from "Interfaces/review.interface";
import Review, { reviewSchema } from "./review.model";

const productSchema = new mongoose.Schema(
  {
    user: {
      type: mongoose.Schema.Types.ObjectId,
      required: true,
      ref: "User",
    },
    name: {
      type: String,
      required: true,
    },
    image: {
      type: String,
      required: true,
    },
    brand: {
      type: String,
      required: true,
    },
    category: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    reviews: {
      type: [reviewSchema],
      ref: "Review",
      default: [],
    },
    rating: {
      type: Number,
      required: true,
      default: 0,
    },
    numReviews: {
      type: Number,
      required: true,
      default: 0,
    },
    price: {
      type: Number,
      required: true,
      default: 0,
    },
    countInStock: {
      type: Number,
      required: true,
      default: 0,
    },
  },
  {
    timestamps: true,
  }
);

const Product: mongoose.Model<IProduct> = mongoose.model<IProduct>(
  "Product",
  productSchema
);

export default Product;
